// 当前语言
let currentLang = localStorage.getItem('language') || 'zh-CN';

// 翻译函数 - 优化的回退逻辑
function t(key, defaultValue = '') {
    // 1. 尝试当前语言
    const currentTranslations = translations[currentLang];
    if (currentTranslations && currentTranslations[key]) {
        return currentTranslations[key];
    }
    
    // 2. 如果当前语言是中文，回退到英文；否则先回退到英文
    if (currentLang !== 'zh-CN' && translations['en'] && translations['en'][key]) {
        return translations['en'][key];
    }
    
    // 3. 回退到中文
    if (translations['zh-CN'] && translations['zh-CN'][key]) {
        return translations['zh-CN'][key];
    }
    
    // 4. 如果中文也没有，尝试英文（中文用户的情况）
    if (translations['en'] && translations['en'][key]) {
        return translations['en'][key];
    }
    
    // 5. 最后使用默认值或键名
    return defaultValue || key;
}

// 切换语言
function setLanguage(lang) {
    if (translations[lang]) {
        currentLang = lang;
        localStorage.setItem('language', lang);
        updatePageLanguage();
    }
}

// 更新页面语言
function updatePageLanguage() {
    // 更新所有带有data-i18n属性的元素
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        const text = t(key);
        
        if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
            element.placeholder = text;
        } else {
            element.textContent = text;
        }
    });
    
    // 更新带有data-i18n-placeholder属性的元素（专门用于placeholder）
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        const text = t(key);
        element.placeholder = text;
    });
    
    // 更新带有data-i18n-html属性的元素（支持HTML）
    document.querySelectorAll('[data-i18n-html]').forEach(element => {
        const key = element.getAttribute('data-i18n-html');
        element.innerHTML = t(key);
    });
    
    // 更新语言选择器
    document.querySelectorAll('.lang-selector button').forEach(btn => {
        if (btn.dataset.lang === currentLang) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });
    
    // 触发自定义事件
    document.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang: currentLang } }));
}

